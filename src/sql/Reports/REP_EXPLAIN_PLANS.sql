create table REP_EXPLAIN_PLANS( STATEMENT_ID    VARCHAR2(1000), PLAN_ID VARCHAR2(400), TIMESTAMP        DATE, REMARKS   VARCHAR2(1000), OPERATION       VARCHAR2(1000), OPTIONS VARCHAR2(1000), OBJECT_NODE     VARCHAR2(1000), OBJECT_OWNER    VARCHAR2(1000), OBJECT_NAME     VARCHAR2(1000), OBJECT_INSTANCE NUMBER(38), OBJECT_TYPE VARCHAR2(1000), OPTIMIZER       VARCHAR2(1000), SEARCH_COLUMNS  NUMBER, ID      NUMBER(38)      , PARENT_ID     NUMBER(38), DEPTH number(38), POSITION  NUMBER(38), COST        NUMBER(38), CARDINALITY NUMBER(38), BYTES       NUMBER(38), OTHER_TAG  VARCHAR2(1000), PARTITION_START  VARCHAR2(1000), PARTITION_STOP  VARCHAR2(1000), PARTITION_ID    NUMBER(38), DISTRIBUTION        VARCHAR2(1000), CPU_COST        NUMBER(38), IO_COST     NUMBER(38), TEMP_SPACE  NUMBER(38), ACCESS_PREDICATES   VARCHAR2(4000), FILTER_PREDICATES               VARCHAR2(4000), TIME    NUMBER(38), TPC_TRANSACTION_NAME varchar2(20), STATEMENT_HASH_SUM varchar2(4000), BENCHMARK_ITERATION varchar2(2), GATHERED_STATS varchar2(5) )tablespace users;
