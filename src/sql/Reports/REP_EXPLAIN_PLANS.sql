select   pt.STATEMENT_ID
		,pt.PLAN_ID
		,pt.TIMESTAMP
		,pt.REMARKS
		,pt.OPERATION
		,pt.OPTIONS
		,pt.OBJECT_NODE
		,pt.OBJECT_OWNER
		,pt.OBJECT_NAME
		,pt.OBJECT_INSTANCE
		,pt.OBJECT_TYPE
		,pt.OPTIMIZER
		,pt.SEARCH_COLUMNS
		,pt.ID
		,pt.PARENT_ID
		,pt.depth
		,pt.POSITION
		,pt.COST
		,pt.CARDINALITY
		,pt.BYTES
		,pt.OTHER_TAG
		,pt.PARTITION_START
		,pt.PARTITION_STOP
		,pt.PARTITION_ID
		,pt.DISTRIBUTION
		,pt.CPU_COST
		,pt.IO_COST
		,pt.TEMP_SPACE
		,pt.ACCESS_PREDICATES
		,pt.FILTER_PREDICATES
		,pt.TIME
		,'transaction_name',
		'md5_sum',
		'iteration_run',
		'gathered_stats'
from plan_table pt
where plan_id = (
  select max(plan_id)
  from plan_table
  where to_date(to_char(timestamp,'MM/DD/YYYY'),'MM/DD/YYYY') = to_date(to_char(sysdate,'MM/DD/YYYY'),'MM/DD/YYYY')
)
order by id;